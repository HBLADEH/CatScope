function initialLocale(): LocaleCode {
  const stored = localStorage.getItem(storageKey)
  if (stored === 'zh-CN' || stored === 'en-US') {
    return stored
  }
  return navigator.language.toLowerCase().startsWith('zh') ? 'zh-CN' : 'en-US'
}

export const currentLocale = ref<LocaleCode>(initialLocale())

export const languageOptions = computed(() => [
  { label: messages[currentLocale.value]['language.zh'], value: 'zh-CN' },
  { label: messages[currentLocale.value]['language.en'], value: 'en-US' }
])

export function getCurrentLocale(): LocaleCode {
  return currentLocale.value
}

export function setLocale(locale: LocaleCode) {
  currentLocale.value = locale
  localStorage.setItem(storageKey, locale)
  document.documentElement.lang = locale
}

export function t(key: string, vars: MessageVars = {}) {
  const template = messages[currentLocale.value][key] ?? messages['en-US'][key] ?? key
  return template.replace(/\{(\w+)\}/g, (_, name: string) => String(vars[name] ?? `{${name}}`))
}

export function localizePresetName(id: string, fallback: string) {
  const keyByID: Record<string, string> = {
    'all-logs': 'preset.allLogs',
    'errors-only': 'preset.errorsOnly',
    'android-runtime': 'preset.androidRuntime',
    'native-crash': 'preset.nativeCrash',
    'install-errors': 'preset.installErrors',
    'current-package': 'preset.currentPackage'
  }
  const key = keyByID[id]
  return key ? t(key) : fallback
}

export function analysisTypeLabel(type: AnalysisType) {
  return t(`analysis.type.${type}`)
}

export function severityLabel(severity: AnalysisSeverity) {
  return t(`analysis.severity.${severity}`)
}

export function analysisTitle(result: AnalysisResult) {
  switch (result.type) {
    case 'java_crash':
      return result.exceptionType
        ? t('analysis.title.javaCrash', { exception: result.exceptionType })
        : t('analysis.title.javaCrashFallback')
    case 'native_crash':
      return t('analysis.title.nativeCrash', {
        signal: result.signal ? t('analysis.title.nativeSignal', { signal: result.signal }) : '',
        library: result.libraryName ? t('analysis.title.nativeLibrary', { library: result.libraryName }) : ''
      })
    case 'anr':
      return result.packageName
        ? t('analysis.title.anr', { packageName: result.packageName })
        : t('analysis.title.anrFallback')
    case 'jni_error':
      return t('analysis.title.jniError')
    case 'install_error': {
      const code = result.keyFrames?.[0] || result.reason || ''
      return code ? t('analysis.title.installError', { code }) : t('analysis.title.installErrorFallback')
    }
    default:
      return result.title || analysisTypeLabel(result.type)
  }
}

export function analysisSummary(result: AnalysisResult) {
  switch (result.type) {
    case 'java_crash':
      return t('analysis.summary.javaCrash', {
        packageName: result.packageName || t('analysis.summary.javaCrashApp'),
        thread: result.threadName ? t('analysis.summary.javaCrashThread', { thread: result.threadName }) : ''
      })
    case 'native_crash':
      return t('analysis.summary.nativeCrash')
    case 'anr':
      return t('analysis.summary.anr')
    case 'jni_error':
      return t('analysis.summary.jniError')
    case 'install_error':
      return t('analysis.summary.installError')
    default:
      return result.summary
  }
}

export function analysisSuggestions(result: AnalysisResult) {
  const keysByType: Partial<Record<AnalysisType, string[]>> = {
    java_crash: ['analysis.suggestion.javaCrash.1', 'analysis.suggestion.javaCrash.2', 'analysis.suggestion.javaCrash.3'],
    native_crash: ['analysis.suggestion.nativeCrash.1', 'analysis.suggestion.nativeCrash.2', 'analysis.suggestion.nativeCrash.3'],
    anr: ['analysis.suggestion.anr.1', 'analysis.suggestion.anr.2', 'analysis.suggestion.anr.3'],
    jni_error: ['analysis.suggestion.jni.1', 'analysis.suggestion.jni.2', 'analysis.suggestion.jni.3'],
    install_error: ['analysis.suggestion.install.1', 'analysis.suggestion.install.2', 'analysis.suggestion.install.3']
  }
  const keys = keysByType[result.type]
  return keys ? keys.map((key) => t(key)) : (result.suggestions ?? [])
}

